export interface MenuItem {
  id: string;
  name: string;
  description: string;
  price: number;
  image: string;
  category: MenuCategory;
  isVeg: boolean;
  isSpicy: boolean;
}

export interface CartItem extends MenuItem {
  quantity: number;
}

export interface OrderItem {
  id: string;
  name: string;
  quantity: number;
  price: number;
  notes?: string;
}

export interface Order {
  id: string; // The '?' is removed to match kitchen. ID will be generated by Firestore.
  tableNumber: number;
  items: OrderItem[];
  status: 'pending' | 'preparing' | 'ready' | 'served';
  orderTime: number; // Switched from timestamp
  estimatedTime?: number; // ETA in minutes
  etaSetAt?: number; // Timestamp when ETA was set
  totalAmount: number; // Switched from total
  customerName?: string;
  specialInstructions?: string;
}

